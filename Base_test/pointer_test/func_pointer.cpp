#include <iostream>

using namespace std;

void (*pfunc)(int);

typedef void (*func)(void);

void myfunc(void)
{
    cout << "func test" << endl;
}

void glfunc(int arg){
    cout << arg << endl;
}

int main(){
    func pfun = myfunc;
    pfun();

    pfunc = glfunc;
    pfunc(2); // or (*pfunc)(2);
}