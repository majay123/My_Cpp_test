#include <iostream>

using namespace std;

class Derived;

class Base
{
public:
    virtual void fun() { cout << "Base fun"; }
};

class Derived : public Base
{
private:
    void fun() { cout << "Derived fun"; }
};

int main(void)
{
    Base *ptr = new Derived;

    ptr->fun();

    return 0;
}