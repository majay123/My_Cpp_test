#include <iostream>
#include <climits>

using namespace std;

class Base
{
public:
    void f() { g(); }
    virtual void g() { cout << "B::g()\n"; }
};

class D : public Base{
    public:
    void g() { cout << "D::g()\n"; }
};

int main(int argc, char const *argv[])
{
    D d;
    d.f();
    return 0;
}